public class Store {

    private int storeID;
    private String name;
    private String location;
    private String openingHours;

    public Store(int storeID, String name, String location, String openingHours) {
        this.storeID = storeID;
        this.name = name;
        this.location = location;
        this.openingHours = openingHours;
    }
}
